package com.sms.model;

public class Marks {
	private String student_id;
	
	private String sinhala;
	private String english;
	private String tamil;
	private String mathematics;
	private String science;
	private String computer_science;
	private String commerce;
	private String music;
	private String dancing;
	private String drawing;
	private String health;
	private String geography;
	private String history;
	private String religion;
	
	public Marks(){}

	public Marks(String student_id, String sinhala, String english, String tamil, String mathematics, String science,
			String computer_science, String commerce, String music, String dancing, String drawing, String health,
			String geography, String history, String religion) {
		super();
		this.student_id = student_id;
		this.sinhala = sinhala;
		this.english = english;
		this.tamil = tamil;
		this.mathematics = mathematics;
		this.science = science;
		this.computer_science = computer_science;
		this.commerce = commerce;
		this.music = music;
		this.dancing = dancing;
		this.drawing = drawing;
		this.health = health;
		this.geography = geography;
		this.history = history;
		this.religion = religion;
	}

	public String getStudent_id() {
		return student_id;
	}

	public void setStudent_id(String student_id) {
		this.student_id = student_id;
	}

	public String getSinhala() {
		return sinhala;
	}

	public void setSinhala(String sinhala) {
		this.sinhala = sinhala;
	}

	public String getEnglish() {
		return english;
	}

	public void setEnglish(String english) {
		this.english = english;
	}

	public String getTamil() {
		return tamil;
	}

	public void setTamil(String tamil) {
		this.tamil = tamil;
	}

	public String getMathematics() {
		return mathematics;
	}

	public void setMathematics(String mathematics) {
		this.mathematics = mathematics;
	}

	public String getScience() {
		return science;
	}

	public void setScience(String science) {
		this.science = science;
	}

	public String getComputer_science() {
		return computer_science;
	}

	public void setComputer_science(String computer_science) {
		this.computer_science = computer_science;
	}

	public String getCommerce() {
		return commerce;
	}

	public void setCommerce(String commerce) {
		this.commerce = commerce;
	}

	public String getMusic() {
		return music;
	}

	public void setMusic(String music) {
		this.music = music;
	}

	public String getDancing() {
		return dancing;
	}

	public void setDancing(String dancing) {
		this.dancing = dancing;
	}

	public String getDrawing() {
		return drawing;
	}

	public void setDrawing(String drawing) {
		this.drawing = drawing;
	}

	public String getHealth() {
		return health;
	}

	public void setHealth(String health) {
		this.health = health;
	}

	public String getGeography() {
		return geography;
	}

	public void setGeography(String geography) {
		this.geography = geography;
	}

	public String getHistory() {
		return history;
	}

	public void setHistory(String history) {
		this.history = history;
	}

	public String getReligion() {
		return religion;
	}

	public void setReligion(String religion) {
		this.religion = religion;
	}

	@Override
	public String toString() {
		return "Marks [student_id=" + student_id + ", sinhala=" + sinhala + ", english=" + english + ", tamil=" + tamil
				+ ", mathematics=" + mathematics + ", science=" + science + ", computer_science=" + computer_science
				+ ", commerce=" + commerce + ", music=" + music + ", dancing=" + dancing + ", drawing=" + drawing
				+ ", health=" + health + ", geography=" + geography + ", history=" + history + ", religion=" + religion
				+ "]";
	}

	
	
	
	
	
	
}
